function djb2(id: string): number {
  let hash = 5381;
  for (let i = 0; i < id.length; i++) {
    hash = (hash * 33) ^ id.charCodeAt(i);
  }
  return hash >>> 0;
}

export function codenameFromSubmissionId(id: string): string {
  const h = djb2(id);
  const a = ADJECTIVES[h % ADJECTIVES.length]!;
  const b = NOUNS[(h >> 7) % NOUNS.length]!;
  return `${a}${b}`;
}
